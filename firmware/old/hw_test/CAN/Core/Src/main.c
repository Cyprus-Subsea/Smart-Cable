bool CANSendFrame(unsigned long id, unsigned char len, const unsigned char *buf, bool wait_sent)
{
	if(HAL_CAN_GetTxMailboxesFreeLevel(&hcan1)>0)
	{
	 TxHeader.ExtId = id;
     TxHeader.RTR = CAN_RTR_DATA;
     TxHeader.IDE = CAN_ID_EXT;
     TxHeader.DLC = len;
     TxHeader.TransmitGlobalTime = DISABLE;
     HAL_CAN_AddTxMessage(&hcan1, &TxHeader, (uint8_t*)buf, &TxMailbox);
     return true;
	}
	return false;
}


bool CANGetFrame(unsigned long* id, unsigned char* len, unsigned char *buf)
{
	 if(HAL_CAN_GetRxFifoFillLevel(&hcan1,CAN_RX_FIFO0)>0)
	 {
		 HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &RxHeader, buf);
		 *len=RxHeader.DLC;
		 *id=RxHeader.ExtId;
		 return true;
	 }
	 else if(HAL_CAN_GetRxFifoFillLevel(&hcan1,CAN_RX_FIFO1)>0)
	 {
		 HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &RxHeader, buf);
		 *len=RxHeader.DLC;
		 *id=RxHeader.ExtId;
		 return true;
	 }

	 return false;
}



